using System;
using System.Collections.Generic;
using System.Text;
using System.Security.Cryptography;

namespace Helper
{
    public class EN
    {
        private static string key = "ABCDEFGH";

        static EN()
        {
            //
            // TODO: 在此处添加构造函数逻辑
